function showTerms() {
    let overlay = document.getElementById("termsOverlay");
    let popup = document.getElementById("termsPopup");
    overlay.style.display = "block";
    popup.style.display = "block";
  }

  function closeTerms() {
    let overlay = document.getElementById("termsOverlay");
    let popup = document.getElementById("termsPopup");
    overlay.style.display = "none";
    popup.style.display = "none";
  }